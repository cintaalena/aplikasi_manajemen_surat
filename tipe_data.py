# Boolean
boolean = True
print("Boolean :", boolean, "->", type(boolean))

# String
string = "Halo, Python!"
print("String  :", string, "->", type(string))

# Integer
integer = 25
print("Integer :", integer, "->", type(integer))

# Float
float_num = 3.14
print("Float   :", float_num, "->", type(float_num))

# Hexadecimal (ditulis dengan awalan 0x, tersimpan sebagai integer)
hexadecimal = 0x1A
print("Hexadecimal :", hexadecimal, "->", hex(hexadecimal), "->", type(hexadecimal))
